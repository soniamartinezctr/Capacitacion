                <h2><span class="color-conductor">Presentación</span></h2>
                <p>Los medios de comunicación ofrecen una variedad de contenidos y formatos dirigidos a todo tipo de usuarios. Los programas se proyectan de acuerdo con la edad, gustos, intereses, horarios y necesidades del sector al que se dirigen. Muchos de ellos requieren de la participación de especialistas que generen confianza y credibilidad, que impacten en el público, para emitir los mensajes que se desean transmitir. Es así que el conductor de televisión y/o el locutor de radio, se convierte en un líder de opinión frente a una audiencia que lo ve y lo escucha, con quien se establece el intercambio de información objetiva e imparcial sobre temas de interés a la sociedad.</p>

                <p>Su papel adquiere un gran valor, debido a que se le considera como una persona de confianza, especialista en temas de interés público, ya sea de información general, política, educativa o cultural, entre otros, que se dirige a una audiencia o televidente en particular, con base en objetivos específicos, por lo que debe tener un buen manejo y cuidado del lenguaje y de la dicción, pero sobre todo de los aspectos éticos para el ejercicio de su función.</p>

                <p>Su actividad se realiza a través de diferentes medios electrónicos y su alcance y penetración es muy amplio, por lo cual tiene una gran responsabilidad social pues requiere de formación específica sobre los temas o el ámbito de información en el que se desenvuelve. Aunque su actividad es principalmente oral, no basta con sólo tener fluidez al hablar ya que también debe conocer los temas de los que se habla, contar con habilidades para informar y opinar, y dimensionar el impacto que tienen sus palabras en el grupo o comunidad a la que se dirigen.</p>

                <p>Por esto el Centro de Capacitación Televisiva, interesado en emprender innovaciones en su oferta educativa y en los contenidos, métodos y procesos de formación para sus usuarios, pone al alcance de los interesados el Diplomado de Conductor y Locutor para Radio y Televisión, con el fin de acercarlos a los medios de comunicación y a las dinámicas de trabajo que acompañan su labor, con énfasis en programas educativos y de difusión cultural.</p>

                <h2><a name="a"><span class="color-conductor">1. Dirigido a</span></a></h2>
                <p>Locutores y conductores con deseos de profesionalizar su actividad laboral, así como especialistas en el campo de la educación, comunicación, marketing, publicidad e interesados en el manejo expresivo ante público, en los ámbitos televisivos y de radiodifusión.</p>

                <h2><a name="b"><span class="color-conductor">2. Perfil de ingreso</span></a></h2>
                <p>Nivel académico mínimo de educación media superior o superior.</p>

                <h2><a name="c"><span class="color-conductor">3. Perfil de egreso</span></a></h2>
                <p>Al término del diplomado, el participante será capaz de:</p>

                <p>Desempeñar la conducción o locución de programas de televisión y radio con base en el dominio de la expresión verbal y corporal, con los estándares de calidad que establece la producción, con ética en el manejo del lenguaje y de la información.</p>


                <h2><a name="d"><span class="color-conductor">4. Documento obtenido</span></a></h2>
                <h3>Diploma</h3>
                <p>Este documento respalda los conocimientos técnicos y teóricos en el campo de la conducción y de la locución.</p>

                <h2><a name="e"><span class="color-conductor">5. Mercado laboral</span></a></h2>
                <p>El ámbito laboral en el que se puede desempeñar el conductor y locutor se ubica en empresas públicas y privadas dedicadas a la producción de programas de televisión y radiodifusión, así como en empresas enfocadas al desarrollo de audiovisuales.</p>

                <h2><a name="f"><span class="color-conductor">6. Duración</span></a></h2>
                <p>180 horas, equivalentes a 18 créditos del Marco Mexicano de Cualificaciones.</p>

                <h2><a name="g"><span class="color-conductor">7. Competencias que se favorecen</span></a></h2>
                <h3>Competencias profesionales</h3>
                <p>Organiza y maneja la información que brinda a través de los medios electrónicos, de acuerdo con los objetivos de la producción. Se desempeña ante cámaras y micrófonos en apego al código de ética de la institución. Improvisa diálogos con base en el análisis de situaciones inesperadas que requieran de sostener la transmisión del programa. Asume la responsabilidad de la información, comentarios y mensajes que emite, ya sean de propia creación o dispuestos a través del guion.</p>

                <h3>Conocimientos</h3>
                <p>Se involucra en el proceso de producción de un programa educativo de radio y de televisión. Reconoce los diferentes formatos de programas de radio y televisión y se desempeña de acuerdo con los requerimientos del mismo, tomando en cuenta el concepto, el público y contenido del mismo. Explica la normatividad aplicable a su función, de acuerdo con la Ley Federal de Radio y Televisión y los principios éticos de producción en el desarrollo de las actividades de conducción y locución, en diferentes géneros de programas de televisión y radio o de otros medios electrónicos.</p>

                <h3>Habilidades</h3>
                <p>Proyecta a través de su voz, del dominio de su cuerpo y del manejo de la información, el concepto del programa de radio o televisión que conduce, de acuerdo con los guiones de producción. Resuelve situaciones repentinas en el estudio, que requieren de improvisación, con dominio en el manejo de la expresión corporal, gestual y verbal.</p>

                <h3>Actitudes</h3>
                <p>Reconoce el impacto de las opiniones, comentarios e ideas que emite y asume la responsabilidad de sus palabras y acciones. Valora la apertura al trabajo colaborativo con el equipo de producción de programas de televisión y radio con base en el respeto y responsabilidad, en atención a la audiencia.</p>

                <p>Las competencias alcanzadas corresponden al Nivel 6 del Marco Mexicano de Cualificaciones.</p>

                <h3>Competencias específicas</h3>
                <p>Las competencias a promover en cada módulo son las siguientes:</p>

                <p>Módulo I. Reconoce la función de los medios de comunicación y la responsabilidad de los conductores y los locutores como líderes de opinión. Explica la normatividad aplicable a los medios, relacionada con su labor comunicadora. Distingue los procesos de producción de un programa de radio y de televisión.</p>

                <p>Módulo II. Comunica mensajes ante cámaras y micrófonos mediante el manejo de la voz, la gesticulación y el movimiento corporal de acuerdo con el guion y con las indicaciones de la producción del programa, en congruencia con el género y el formato del mismo.</p>

                <p>Módulo III. Controla y utiliza adecuadamente y con ética su lenguaje oral y corporal en la conducción y locución en televisión y radio, con base en las indicaciones de producción.</p>

                <h2 class="color_con"><a name="h"><span class="color-conductor">8. Organización</span></a></h2>
                <p>El diplomado está organizado en tres módulos secuenciados, compuestos por dos cursos de 20 horas cada uno en el primer módulo; dos cursos de 40 horas cada uno para el segundo y dos cursos de 30 horas cada uno para el tercero.</p>

                <p>El primer módulo se caracteriza por introducir al participante en el análisis del impacto social que tienen las funciones de conducción y locución, a partir de las características de cada medio y de otros medios tecnológicos.</p>

                <p>En el módulo II se abordan el dominio de las técnicas de comunicación con audiencias de televisión y radio, a partir de los guiones, de los formatos y de la normatividad establecida.</p>

                <p>En el módulo III el participante realiza prácticas de conducción y locución de programas de televisión y radio y se familiariza con los procesos de producción de los mismos.</p>

                <h2><a name="i"><span class="color-conductor">9. Metodología</span></a></h2>
                <p>La modalidad de impartición es presencial, conformada por sesiones de 4 horas cada una en las que se intercambian experiencias entre los participantes y el asesor del curso. Se realizan constantes prácticas en cabina y en estudio en las que los participantes se relacionan de manera directa con la dinámica de producción de programas de radio y de televisión.</p>

