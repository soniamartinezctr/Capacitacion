    <h2 class="color_cam">Presentación</h2>
    <p><p>Nuestra vida transcurre en medio de una cantidad desbordante y colorida de imágenes y sonidos enmarcados en audiovisuales que la tecnología nos proporciona. En la actualidad, modernas cámaras de video permiten captar y resguardar imágenes que se convierten en testimonio de la vida cotidiana. Sin embargo, el trabajo de registro de imágenes no es sencillo y requiere de capacitación en el uso de la cámara, de conocimiento del lenguaje audiovisual y de técnicas de trabajo adecuadas para aplicar en estudio o locación.</p>

    <p>Para el caso de la televisión educativa se requiere de camarógrafos especializados que sean responsables de dar vida a la imagen, en una narrativa audiovisual que derive de una intención específica y del manejo de los recursos que utiliza; lleva a imágenes el guion técnico, cuidando el sentido que se le quiera dar al discurso televisivo. De esta manera, una misma imagen puede producir un efecto aterrador o humanitario dependiendo de la manera como se capta.</p>

    <p>Los factores que influyen y dan personalidad a la imagen son muy variados: el movimiento de la cámara, el tamaño de la toma, el encuadre, el ángulo, pero la imagen también cobra sentido en función de la toma que le antecede y la que le sucede, a través de la composición y la edición. El movimiento junto con el audio y la narración contribuyen a formar un cuerpo vivo y completo, con personalidad. Así, la cámara se transforma en el ojo del espectador.</p>

    <p>Es por todo esto que el Centro de Capacitación Televisiva se ha esforzado por emprender innovaciones en su oferta educativa, como también en los contenidos, métodos y procesos de formación para sus usuarios a través del Diplomado Camarógrafo de Televisión en Estudio y Locación, con el fin de fortalecer competencias en grabación, tanto en estudio como en locación, a través del manejo de la cámara de video P2 (AG-HPX500P2).</p>

    <h2 class="color_cam"><a name="a">1. Dirigido a</a></h2>
    <p>Camarógrafos, editores/posproductores, técnicos y profesionales de la comunicación, e interesados en el trabajo de la operación de cámaras video.</p>

    <h2 class="color_cam"><a name="b">2. Perfil de ingreso<a name="a"></h2>
                <p>Nivel académico mínimo de educación media superior o superior.</p>

                <h2 class="color_cam"><a name="c">3. Perfil de egreso</a></h2>
                <p>Al finalizar el diplomado, el participante será capaz de:</p>

                <p>Ejecutar de manera creativa, planes de trabajo y operación de cámara en producciones audiovisuales porque la cámara implica el registro de ambos tanto en estudio como en locación.
                </p>

                <p>Aplicar los diferentes elementos del lenguaje audiovisual, como el encuadre, el plano, el ángulo, la composición y los movimientos de cámara, de acuerdo con la intención de la producción audiovisual.
                </p>

                <h2 class="color_cam"><a name="d">4. Documento obtenido</a></h2>
                <h3>Diploma</h3>
                <p>Este documento respalda los conocimientos técnicos y teóricos, necesarios para el desempeño idóneo del camarógrafo de televisión en estudio y locación.</p>

                <h2 class="color_cam"><a name="e">5. Mercado laboral</a></h2>
                <p>Un camarógrafo puede desempeñarse en televisoras públicas o privadas, comerciales o educativas, así como en empresas productoras de video, y contribuir al logro de los objetivos de la producción.</p>

                <h2 class="color_cam"><a name="f">6. Duración</a></h2>
                <p>240 horas, equivalentes a 24 créditos del Marco Mexicano de Cualificaciones</p>

                <h2 class="color_cam"><a name="g">7. Competencias que se favorecen</a></h2>
                <h3>Competencias profesionales</h3>
                <p>Gestiona proyectos técnicos relativos al registro de imagen para programas de televisión, que implican el dominio del lenguaje audiovisual para dar sentido a la narrativa y responder a la intencionalidad del programa, con énfasis en producciones educativas. Maneja la tecnología de equipos de cámara y aplica diversas técnicas de captura. Resuelve y toma decisiones sobre situaciones emergentes referentes al campo de la imagen y a la tecnología que se utiliza, tanto en estudio como en locación, en el marco del flujo de trabajo con el equipo de producción.</p>

                <h3>Conocimientos</h3>
                <p>Explica principios y técnicas de grabación de imagen para diversas producciones audiovisuales, tanto en estudio como en locación, que requiere la comprensión crítica de elementos de producción y edición del lenguaje audiovisual. Define la estructura, operación y funcionamientos del equipo de cámara de video P2 (AG-HPX500P2), que permitan la captura de imágenes. Adquiere nociones de iluminación y audio para la operación de la cámara.</p>

                <h3>Habilidades</h3>
                <p>Captura imágenes de acuerdo con el guion técnico y el plan de producción, tanto en estudio como en locación. Toma decisiones con respecto al registro de la imagen en el marco del flujo de trabajo para el diseño de la escena y la narrativa visual. Resuelve situaciones imprevistas relacionadas con el funcionamiento del equipo y el tipo de transmisión.</p>

                <h3>Actitudes</h3>
                <p>Valora la importancia de la imagen en la narrativa audiovisual y su impacto en el espectador. Comunica de forma eficiente las necesidades sobre la labor que desempeña. Mantiene un ambiente de trabajo cordial a partir del respeto y trabajo colaborativo con el equipo de producción. Mantiene las normas de seguridad e higiene en los procesos. </p>

                <p>Las competencias alcanzadas corresponden al Nivel 7 del Marco Mexicano de Cualificaciones.</p>

                <h3>Competencias específicas</h3>
                <p>Las competencias a promover en cada módulo son las siguientes:</p>

                <p>Módulo I. Explica la estructura, operación y funcionamiento de la cámara de video P2 (AG-HPX500P2), y los utiliza en levantamiento de imágenes, tanto en estudio como en locación, integrando las nociones de audio e iluminación, así como los elementos del lenguaje audiovisual.</p>

                <p>Módulo II. Reconoce las características y requerimientos de grabación de imagen establecidos en el guion técnico y los aplica en la grabación tanto en locación como en estudio, en apego a los procesos definidos por la producción. Evalúa sus productos de acuerdo con el guion técnico y con las normas sobre manejo de imagen.</p>

                <p>Módulo III. Aplica los procedimientos de grabación en el marco de la ingeniería de la producción audiovisual,[1] conforme a los lineamientos establecidos en el plan de trabajo para distintos tipos de audiovisual.</p>

                <h2 class="color_cam"><a name="h">8. Organización</a></h2>
                <p>El diplomado está organizado en tres módulos secuenciados, el primero, compuesto por cuatro cursos de 20 horas cada uno, responde a la formación básica que un camarógrafo debe poseer, donde se imparten los fundamentos para el desarrollo de su función y se abordan nociones del campo de la iluminación y del audio, así como de lenguaje audiovisual.</p>


                <p>El módulo II se compone de dos cursos de 40 horas de duración cada uno; en ellos se explica el proceso de grabación de imagen en diferentes contextos, ya sea en estudio o en locación, en espacios abiertos y/o cerrados, con base en los requerimientos de producción. Asimismo se definen estrategias de grabación de programas para la obtención de mejores resultados y el cumplimiento de los objetivos establecidos.</p>

                <p>Módulo II: en donde se estudia la importancia y naturaleza de los centros de información, la organización física de los mismos, las técnicas utilizadas para la organización y agrupación, así como el impacto de las nuevas tecnologías en la documentación audiovisual.</p>

                <p>En el módulo III, compuesto por dos talleres de 40 horas cada uno, se busca que el participante comprenda y se integre en el flujo de trabajo en el marco de la ingeniería de la producción, se desempeñe adecuadamente según los requerimientos del contexto y los objetivos de grabación, con base en la implementación de procedimientos de seguridad e higiene, así como los establecidos en el guion técnico.</p>

                <h2 class="color_cam"><a name="i">9. Metodología</a></h2>
                <p>Para favorecer las competencias planteadas, el diplomado integra un conjunto de cursos y talleres teórico-prácticos en los que el participante practica en situaciones reales, interactuando con un equipo de producción (sonidistas, productor, iluminador, floor manager, entre otros), para aplicar los conocimientos adquiridos y desenvolverse con base en procedimientos y lineamientos de calidad.</p>

                <p>La modalidad de impartición es presencial, conformada por sesiones de 4 horas, en las que se intercambian experiencias entre los participantes y el asesor del curso. Se realizan actividades de aprendizaje orientadas al fortalecimiento de habilidades en el manejo y operación de la cámara ya sea en estudio y/o locación. En los talleres se busca la integración de las áreas de ingeniería de la producción a fin de brindar experiencias en condiciones reales de trabajo.</p>


