            <h2><span class="rojo">Presentación</span></h2>
            <p>La televisión representa un medio de alto impacto en el desarrollo de la comunicación, la información, la educación, el entretenimiento y la cultura; por la naturaleza de su responsabilidad en la selección, producción y transmisión de mensajes y contenidos, el medio requiere de un equipo interdisciplinario de trabajo, donde la figura del guionista es primordial, ya que genera la materia prima de lo que se verá en pantalla. En su ejercicio creativo, el guionista se encarga de diseñar, estructurar y elaborar libretos o guiones que proyecten imaginación y talento; mediante el uso adecuado de géneros y la innovación en formatos televisivos.</p>
            <p>Para la creación de audiovisuales es primordial que el guionista, además de poseer los conocimientos y habilidades necesarios para el desarrollo de su oficio, tenga una actitud desafiante para exponer y dosificar los contenidos necesarios para estructurar su guion, de tal forma que el espectador reciba la información que visualiza y mantenga el interés en el audiovisual.</p>
            <p>Los distintos medios audiovisuales demandan formatos propositivos que cautiven al espectador; la televisión no es ajena a esta exigencia, por ello, el Centro de Capacitación Televisiva pone a disposición de la comunidad el Diplomado Guionista de Televisión, con el fin de formar guionistas que respondan a las necesidades comunicativas y de información que las instituciones públicas y privadas demandan.</p>


            <h2><a name="a"><span class="rojo">1. Dirigido a</span></a></h2>
            <p>Guionistas de televisión y otros medios de producción audiovisual.</p>

            <h2><a name="b"><span class="rojo">2. Perfil de ingreso</span></a></h2>
            <p>Nivel académico de educación superior.</p>
            <h2 class="color_guio"><a name="c"><span class="rojo">3. Perfil de egreso</span></a></h2>
            <p>Al término del diplomado, el participante será capaz de:</p>

            <p>Planear, diseñar y elaborar guiones o libretos, utilizando estructuras y formatos adecuados e innovadores para presentar productos audiovisuales de géneros dramáticos y documentales.

            <h2><a name="d"><span class="rojo">4. Documento obtenido</span></a></h2>
            <h3>Diploma</h3>
            <p>Este documento respalda los conocimientos técnicos y teóricos, para la elaboración de guiones para televisión y otros medios audiovisuales.</p>

            <h2><a name="e"><span class="rojo">5. Mercado laboral</span></a></h2>
            <p>Un guionista puede desarrollarse en televisoras, productoras e instituciones públicas y privadas que produzcan materiales audiovisuales de distintos géneros y formatos.</p><h2 class="color_guio"><a name="f"><span class="rojo">6. Duración</span></a></h2>
            <p>220 horas, equivalentes a 22 créditos del Marco Mexicano de Cualificaciones.</p>

            <h2><a name="g"><span class="rojo">7. Competencias que se favorecen</span></a></h2>
            <h3>Competencias profesionales</h3>
            <p>Propone estructuras narrativas innovadoras en el diseño de guiones para audiovisuales, a partir del dominio de la narrativa en géneros y formatos televisivos. Transforma los contenidos en guiones en colaboración continua con el equipo de producción.</p>

            <h3>Conocimientos</h3>
            <p>Examina las características y elementos del lenguaje audiovisual, los géneros dramáticos y documentales a partir de teorías de la comunicación y corrientes estéticas. Explica las bases de la poética y la retórica para la creación de libretos.</p>

            <h3>Habilidades</h3>
            <p>Diseña y redacta guiones para programas de televisión con diversos géneros y formatos, a partir de un tema o contenido, y en función del propósito de la producción. Transforma la narrativa audiovisual de acuerdo con las características del público al que se dirige.</p>

            <h3>Actitudes</h3>
            <p>Valora el impacto social de la televisión y otros medios audiovisuales en el planteamiento de los guiones para programas audiovisuales. Reconoce la importancia de los estímulos audiovisuales como parte fundamental de la relación de los usuarios con el medio.</p>

            <p>Las competencias alcanzadas corresponden al Nivel 7 del Marco Mexicano de Cualificaciones.</p>

            <h3>Competencias específicas</h3>
            <p>Las competencias a promover en cada módulo son las siguientes:</p>

            <p>Módulo I. Reconoce el potencial creativo que tiene como guionista de televisión. Describe las características del guion, considerando los elementos artísticos y de la comunicación presentes en el libreto.</p>
            <p>Módulo II. Elige los conceptos narrativos y aplica las técnicas dramáticas y documentales al elaborar guiones para programas unitarios y series de televisión, en función de los contenidos y propósitos de la producción.</p>
            <p>Módulo III. Elabora guiones para audiovisuales, series y programas de televisión con base en procesos de investigación y diseño dramático que respondan a las necesidades de la producción y características del público objetivo.</p>

            <h2><a name="h"><span class="rojo">8. Organización</span></a></h2>
            <p>El diplomado está organizado en tres módulos secuenciados e integrados a su vez, por tres cursos cada uno. El trabajo que requiere el primero es de 60 horas, el segundo es de 70 horas y el tercero es de 90 horas.</p>

            <p>En el módulo I se analizan conceptos clave de la función del guionista, del lenguaje audiovisual, el arte y el guion como documento creativo, así como los elementos fundamentales de la comunicación.</p>

            <p>En el módulo II se estudian los elementos narrativos y las estructuras dramáticas para generar sinergias de alto nivel de creatividad en el desarrollo temático de las historias dramáticas y documentales.</p>

            <p>En el módulo III se desarrolla la práctica continua en la elaboración de guiones, a partir de un proceso de investigación y planificación que lleve a los participantes a la redacción de libretos de diversos géneros y formatos.</p>

            <h2><a name="i"><span class="rojo">9. Metodología</span></a></h2>
            <p>Para favorecer el logro de los objetivos y competencias planteadas, el Diplomado Guionista de Televisión se lleva a cabo a través de cursos y talleres teórico-prácticos, en los que el participante se desempeñará como guionista de televisión con base en los conocimientos adquiridos.</p>

            <p>La modalidad es presencial, conformada por sesiones de 4 horas en las que se intercambian experiencias entre los participantes y el asesor del curso y se realizan actividades de aprendizaje, orientadas al fortalecimiento de habilidades para el diseño y redacción de guiones, para la producción de audiovisuales. En el diplomado se fortalece la práctica de la escritura creativa de guiones a través de talleres.</p>

