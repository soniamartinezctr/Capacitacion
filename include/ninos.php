        <div class="row">
            <h3 class="col-md-9" align ="left">TALLERES</h3>
            <br>
            <h3 class="col-md-9" align ="left">Un dia en la tele</h3>
            <p class="col-md-9">
                El taller es una actividad gratuita que tiene como objetivo desarrollar una experiencia de observación,
                acercamiento y juego con los diversos espacios, personas y equipo tecnológico que intervienen en la 
                producción televisiva.
            </p>
            <br>
            <p class="col-md-9">
                En este sentido, busca familiarizar a los niños con el lenguaje y los elementos que se emplean dentro de la
                elaboración de un programa de televisión. Así como dar a conocer el universo de personas, prácticas e 
                instrumentos situados detrás de las pantallas, pretendiendo generar una visión humanizada y 
                sociocultural de la TV.
            </p>
            <br>
            <p class="col-md-9">
                Un Día en la Tele también contempla el desarrollo de diversos ejercicios y tareas en los que se explota el 
                sentido lúdico,la creatividad y el trabajo en equipo. Dentro de las actividades se abre un espacio para
                reflexionar acerca de la forma y fondo de los distintos programas de televisión que los participantes ven
                en su cotidianidad.
            </p>
            <br>
            <h3 class="col-md-9" align ="left">Descripción de la actividad</h3>
            <p class="col-md-9">
                Al inicio del taller el niño comparte los demás integrantes los programas de televisión que ve y
                analiza críticamente junto con ellos, apoyado de materiales audiovisuales, los contenidos de éstos.
                Después conoce y diferencia los roles y las funciones de las personas, que trabajan en la televisión.
                Se le muestran los equipos electrónicos y el lugar donde se trabajará: posteriormente el grupo graba su
                propio programa de televisión educativo asumiendo diferentes roles como parte de un staff de producción.
                Por último, se refuerza el conocimiento adquirido a través del juego y la reflexión.
            </p>

