            <p class="col-md-9"> 
                Dentro de una atmósfera de juego, creatividad, reflexión, análisis y experimentación, el <b>Centro de 
                    Capacitación Televisiva</b> abre un espacio para que las niñas y los niños de México viajen hacia el 
                encuentro de una cultura infantil televisiva mediante el <b>Programa de Formación Audiovisual Niños a la Tele.</b>
            </p>
            <p class="col-md-9">
                El programa se propone acercar a los estudiantes de educación básica, al mundo televisivo, para que
                descubran que tras la pantalla, un sofisticado universo tecnológico, humano y creativo da vida a 
                historias o relatos donde la imagen, la voz, la música o el silencio forman parte de ese mundo mágico.
