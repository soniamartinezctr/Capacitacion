        <div class = "row" >
            <h3 class="col-md-12" align ="left">Antecedentes</h3> 
            <p class="col-md-9">
                En el mes de octubre del año 1998, iniciamos en el Centro de Capacitación Televisiva, una experiencia de trabajo inédita en la institución. Se trata de atender a un público infantil considerándolo no como audiencia a quien se dirige una producción de televisión, sino como participantes de un programa de actividades educativas que, al tiempo que son atractivas y divertidas para los niños, puedan generar una relación distinta entre ellos y este medio.
            </p>
            <p class = "col-md-9">
                Nuestro Centro tiene entre sus objetivos la formación, actualización y capacitación de los profesionales que laboran en todos los ámbitos relacionados con los medios audiovisuales, con especial énfasis en el carácter educativo de los mismos.
            </p>

            <p class="col-md-9">
                El Programa de Formación Niños a la Tele se ubica, por lo tanto, en correspondencia a la misión o perfil de la institución. Se trata de formar al público infantil en el conocimiento y uso del medio televisivo, partiendo de reconocer la presencia contundente de este medio en su vida cotidiana y la necesidad de desarrollar una nueva relación con este medio, para propiciar que los niños sean algo más que “simples consumidores”. 
            </p>

            <p class="col-md-9">
                Somos conscientes de las necesidades que van surgiendo en el seno del Sistema Educativo Nacional, a partir de la presencia e incorporación de los medios y las tecnologías de la información y la comunicación en diversos proyectos educativos y en los planteles escolares. Por ello resulta de particular importancia aprovechar el espacio que brinda este proyecto para promover un uso más adecuado de la televisión en los procesos de aprendizaje, para enriquecer los medios con que cuentan los planteles para mostrar, ilustrar y enriquecer los contenidos y, para superar las inercias creadas frente al medio por la mitificación a la que fue sometido hace algunos años.
            </p>
            <p class="col-md-9">
                Por sus características el proyecto cuenta con la aceptación de maestros y directivos de escuelas, por ser considerado como una actividad extraescolar que al romper con la rutina establecida fomenta en los niños inquietud, interés y predisposición, factores favorables para su realización.
            </p>

