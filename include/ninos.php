            <h3 class="col-md-9" align ="left">Características Generales</h3> 
            <p class="col-md-9">
                El proyecto se dirige principalmente a niños y adolescentes que se encuentran en edad escolar de nivel básico.<br>
                Está planeado para trabajar con grupos de 20 a 30 niños por emisión.<br>
                Las actividades que se desarrollan varían en función del grado escolar.<br>
                El equipo de trabajo está integrado por técnicos, profesionales de la educación y la comunicación, preparados para diseñar actividades didácticas, conducir grupos, y que cuentan con el conocimiento de los procesos, recursos y herramientas de la televisión, para hacer posible el proyecto.
