            <div class="row">
                <h4 class="col-md-9" align ="left">Formación de Orientadores Audiovisuales</h4>
                <p class="col-md-9">
                    Para docentes de educación básica, este taller ofrece herramientas conceptuales que permiten
                    comprender con más plenitud el lugar de los medios de comunicación en la cultura contemporánea y, 
                    especialmente, la manera en que el discurso de la televisión participa en la formación global de niños y 
                    niñas. Formación de Orientadores Audiovisuales (FOA) tiene como propósito ofrecer recursos
                    metodológicos concretos que auxilien a los docentes en la incorporación de la televisión y los diversos 
                    medios audiovisuales como parte de la actividad pedagógica en el sentido de cualificar a las niñas y los
                    niños como sujetos creativos y analíticos ante los medios.
                </p>
                <p class="col-md-9">
                    El FOA forma parte de los Servicios Educativos que imparte el Centro de Capacitación Televisiva
                    y se otorga constancia de participación.
                    Para mayor información o inscripción en el mismo, invitamos a directivos y docentes de escuelas de 
                    Educación Básica a estar el pendiente de los periodos de inscripción e impartición.
                </p>
                <h4 class="col-md-9" align ="left">Alcances</h4> 
                <p class="col-md-9">
                    A los largo de estos dieciséis años, el Programa de Formación Audiovisual Niños a la Tele ha atendido a
                    través de sus diversas actividades a más de 70,000 alumnos  de Educación Básica; sin incluir a los 
                    docentes y padres de familia que también han sido participantes activos, de esta forma, Niños a la Tele
                    se ha consolidado como un programa innovador que, además de explotar el sentido lúdico, favorece las
                    relaciones sociales y competencias comunicativas de los alumnos.
                </p> 
