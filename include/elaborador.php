                <h2><a name="g"><span class="color-elaborador">7. Competencias que se favorecen</span></a></h2>
                <h3>Competencias profesionales</h3>
                <p>Desarrolla materiales educativos audiovisuales, con contenidos fundamentados en la disciplina para la cual se proponen. Plantea estrategias congruentes con los usuarios y contenidos e incorpora formas de presentación innovadoras, que faciliten los procesos educativos, académicos y/o de capacitación, con base en diferentes dispositivos y recursos multimedia. Se responsabiliza del desarrollo, implementación, seguimiento y evaluación de la propuesta. Optimiza los recursos con base en el costo beneficio del material.</p>

                <h3>Conocimientos</h3>
                <p>Expresa conocimientos en el campo del diseño de materiales educativos audiovisuales pertinentes a los requerimientos de los diferentes tipos y modalidades de educación, para promover procesos educativos innovadores en algún campo del conocimiento. Manifiesta conciencia crítica sobre cuestiones de educación articuladas con el diseño instruccional de recursos multimedia y su aplicación.</p>

                <h3>Habilidades</h3>
                <p>Analiza, planea y diseña estrategias de aprendizaje y/o de enseñanza con base en el material educativo diseñado y evalúa su potencial educativo en contextos diversos. Comunica sus ideas sobre la pertinencia del uso de materiales audiovisuales en procesos educativos con base en su estructura y diseño, así como de sus componentes y sus perspectivas de uso en diferentes ambientes de aprendizaje.</p>

                <h3>Actitudes</h3>
                <p>Organiza acciones para la incorporación de recursos audiovisuales en procesos educativos a fin de que se utilicen de forma persistente para favorecer competencias en los usuarios. Comunica a sus pares los conocimientos y habilidades que se requieren para el manejo de los materiales. Demuestra las potencialidades de los materiales que diseña.</p>

                <p>Las competencias alcanzadas corresponden al Nivel 7 del Marco Mexicano de Cualificaciones.</p>

                <h3>Competencias específicas</h3>
                <p>Las competencias a promover en cada módulo son las siguientes:</p>

                <p>Módulo I. Explica las potencialidades educativas de los recursos multimedia, con base en el análisis del lenguaje audiovisual que llevan implícitos y el perfil de los usuarios a quienes se dirigen. </p>

                <p>Módulo II. Distingue los procesos cognitivos implícitos en el aprendizaje con el fin de seleccionar los recursos multimedia más apropiados, conforme a las intenciones educativas que se persiguen, para fortalecer procesos de enseñanza y aprendizaje.</p>

                <p>Módulo III. Formula proyectos de implementación de recursos multimedia en la educación, que favorezcan procesos educativos para su evaluación y mejora de acuerdo a su pertinencia didáctica.</p>
