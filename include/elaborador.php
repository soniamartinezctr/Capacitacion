                <h2><span class="color-elaborador">Presentación</span></h2>
                <p>En la actualidad la sociedad está expuesta a una gran cantidad de estímulos visuales y auditivos, fijos y en movimiento, en diferentes formas y dispositivos de acceso y para mantenerse en comunicación con otras personas, en todas partes del mundo y prácticamente a cualquier hora. Esta exposición ha hecho que, en muchos casos, la capacidad de atención se vea disminuida y que se tenga la necesidad de estímulos cada vez más llamativos para captarla. Ante esto, los medios de comunicación buscan acercar a los usuarios y retener su atención a través del manejo de imágenes, sonidos y contenidos sustantivos con el fin de informar, entretener o vender ideas y/o productos. </p>

                <p>Conscientes de esta saturación de estímulos y de que en el ámbito educativo la relación con los medios de información y/o comunicación siempre ha sido un reto, se requiere de materiales que faciliten los procesos educativos, de formación y/o de capacitación en los diferentes niveles y modalidades, con contenidos y presentación innovadores, pertinentes y creativos. De esta forma, el elaborador de materiales educativos es el creador de herramientas didácticas que son de gran utilidad a los docentes para estimular y orientar procesos de educación y/o formación y que faciliten el aprendizaje de los estudiantes. </p>

                <p>Es por ello que el Centro de Capacitación Televisiva pone a disposición el Diplomado Elaborador de Materiales Educativos Audiovisuales, que permitirá a los participantes diseñar, implementar y evaluar materiales educativos audiovisuales con un diseño instruccional innovador que coadyuven en los procesos educativos.</p>
