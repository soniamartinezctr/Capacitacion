                <h2><a name="h"><span class="color-elaborador">8. Organización</span></a></h2>
                <p>El diplomado está organizado en tres módulos secuenciados, el primero, compuesto por dos cursos de 20 horas cada uno, responde a la formación básica que comparte con el Diplomado Especialista en Uso de Medios Educativos, donde se imparten los fundamentos para la incorporación de los recursos multimedia en procesos educativos. Al finalizar este módulo el participante continúa sus estudios en su área de especialidad.</p>

                <p>El módulo II se compone de tres cursos de 20 horas cada uno; en ellos se explican los procesos cognitivos que facilitan el aprendizaje con la integración de diferentes recursos multimedia. Se incorporan estrategias que apoyen tales procesos desde el diseño, a partir del conocimiento de las características del usuario y las competencias u objetivos a lograr.</p>

                <p>En el módulo III se transita por cuatro cursos que en total duran 100 horas, con la finalidad de generar proyectos que permitan al participante realizar el diseño, la implementación y la evaluación de propuestas de materiales educativos adecuados a las necesidades y características del programa educativo en el que se inserten.</p>

                <p>Con el fin de dar un carácter integral a la formación en Educación y Medios, se incluye, adicionalmente, un curso en línea optativo con duración de 20 horas.</p>

                <h2><a name="i"><span class="color-elaborador">9. Metodología</span></a></h2>
                <p>Para favorecer los objetivos y competencias planteadas el diplomado consta de un seminario introductorio y ocho cursos formativos. La modalidad de impartición es en línea; las actividades de aprendizaje, que se realizan, se orientan al desarrollo de conocimientos y habilidades.</p>


