                <h2><a name="g"><span class="color-iluminador">7. Competencias que se favorecen</span></a></h2>
                <h3>Competencias profesionales</h3>
                <p>Gestiona proyectos técnicos de iluminación que implican el diseño y la coordinación de la propuesta de ambientación para programas audiovisuales con énfasis en televisión educativa, de acuerdo con el objetivo o intención de las escenas y optimizando la infraestructura y/o recursos disponibles tanto en estudio como en locación. Toma y asume la responsabilidad de las decisiones sobre la colocación de los reflectores y el control de la luz así como de las resoluciones pertinentes ante imprevistos, derivados de la planeación de la propuesta, en el marco del flujo de trabajo de una producción.</p>

                <h3>Conocimientos</h3>
                <p>Explica principios y tecnologías del campo de la iluminación, así como la función en relación con la narrativa audiovisual de la propuesta del guion técnico. Distingue las diferencias de aplicación de equipo y técnicas para iluminar en estudio y en locación. Explica principios de electricidad a fin de hacer un manejo adecuado del equipo y salvaguardar la seguridad en la producción. Adquiere nociones de audio y cámara para ambientar escenas.</p>

                <h3>Habilidades</h3>
                <p>Realiza un esquema de iluminación en el que aplica técnicas de manejo de luz y sombra para establecer el ambiente de la escena, de acuerdo con el requerimiento de la producción. Coordina el montaje y direccionamiento del equipo de iluminación y la controla a través de los dispositivos específicos para este fin.</p>

                <h3>Actitudes</h3>
                <p>Valora la importancia de iluminación en la narrativa audiovisual y su impacto en el espectador. Comunica de forma eficiente las necesidades sobre la labor que desempeña. Mantiene un ambiente de trabajo cordial a partir del respeto y trabajo colaborativo con el equipo de producción. Mantiene las condiciones de seguridad e higiene del equipo de iluminación.</p>

                <p>Las competencias alcanzadas corresponden al Nivel 6 del Marco Mexicano de Cualificaciones.</p>

                <h3>Competencias específicas</h3>
                <p>Las competencias a promover en cada módulo son las siguientes:</p>

                <p>Módulo I. Diseña un esquema básico de iluminación de acuerdo con los objetivos de ambientación de la escena. Selecciona los reflectores, materiales y accesorios de iluminación adecuados, según la intencionalidad del programa. Mantiene el correcto funcionamiento del equipo, con apego a las normas de seguridad e higiene dentro de la producción.</p>

                <p>Módulo II. Realiza el montaje de la iluminación para establecer el ambiente de la escena tanto en estudio como en locación, en el que considera los puntos básicos y el tipo de reflectores que se requieren. Valora las sensaciones y emociones que produce la iluminación, de acuerdo con el guion técnico.</p>

                <p>Módulo III. Implementa los procedimientos para el montaje de la iluminación de acuerdo con el objetivo de ambientación de la producción y a las condiciones de grabación, con apego a las normas de seguridad. Coordina el proceso de iluminación en el marco del flujo de trabajo en estudio y en locación.</p>
