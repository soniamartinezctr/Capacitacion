                <h2><span class="color-iluminador">Presentación</span></h2>
                <p>Los colores, las luces y las sombras, son parte de nuestra vida diaria. De acuerdo con las experiencias y con la formación, cada persona tiene preferencia por ciertos ambientes de iluminación. En algunos casos, las condiciones de luz sugieren emociones, situaciones específicas o sentimientos como: peligro, tristeza, suspenso, romance, entre otros. Los programas audiovisuales requieren del manejo de la luz para provocar tales sensaciones.</p>

                <p>El iluminador es el especialista encargado de crear el ambiente de la escena en producciones audiovisuales, para su grabación o transmisión, mediante herramientas técnico-artísticas de iluminación, para estudio y locación.</p>

                <p>La importancia de su labor radica en establecer las condiciones necesarias de luz (intensidad, contraste, temperatura de color, calidad, color, entre otras) para que una escena pueda verse de forma clara y agradable; destaca la sensación de tridimensionalidad en la imagen, guía al espectador hacia los puntos más relevantes de la escena o cancela aquellos que pueden ser factor de distracción. La luz como tal puede fijar el tiempo de la acción y por lo tanto, establecer el ambiente de acuerdo con el género del programa que defina la producción.</p>

                <p>Por esta razón el Centro de Capacitación Televisiva, pone a disposición el Diplomado Iluminador para Televisión. Este diplomado está conformado por una serie de cursos y talleres enfocados a fortalecer las habilidades técnicas y creativas del iluminador para la realización de un programa audiovisual. Destaca la práctica permanente de técnicas de iluminación con equipos de tecnología actual (reflectores de led, HMI, luz fría entre otros); con el fin de diseñar escenarios adecuados para cualquier tipo de producción audiovisual.</p>

                <p>El participante desarrolla su actividad en el laboratorio de iluminación, el cual cuenta con equipos, materiales y accesorios de primera línea, ideales para realizar todas las actividades relacionadas con la luz en una producción audiovisual.</p>
