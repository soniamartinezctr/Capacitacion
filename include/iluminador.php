                <h2><a name="h"><span class="color-iluminador">8. Organización</span></a></h2>
                <p>El diplomado está organizado en tres módulos secuenciados, el primero, compuesto por tres cursos de 20 horas cada uno, responde a la formación básica que un operador de equipo de iluminación debe conocer, donde se imparten los fundamentos para la operación de equipo de iluminación en estudio y locación; con una o más personas u objetos, fijos o en movimiento, considerando las condiciones de seguridad e higiene de los materiales y de los participantes.</p>

                <p>El módulo II se compone de dos talleres de 30 horas de duración cada uno; en ellos se explica el proceso de iluminación en el marco de una producción, tanto en estudio como en locación, con base en los requerimientos de producción y valora el impacto.</p>

                <p>En el módulo III, compuesto de dos talleres de 40 horas cada uno, se busca que el participante comprenda el flujo de trabajo y se desempeñe con correspondencia de acuerdo con los requerimientos establecidos en el guion técnico y con base en la implementación de procedimientos de seguridad e higiene. Se realiza la valoración técnica y estética de programa con base en los objetivos planteados.</p>

                <h2><a name="i"><span class="color-iluminador">9. Metodología</span></a></h2>
                <p>Para favorecer los objetivos y competencias planteadas, el diplomado parte de cursos y talleres teórico-prácticos en los que el participante se desempeñará con base en los conocimientos adquiridos y en el seguimiento de procedimientos y lineamientos de calidad. </p>

                <p>La modalidad de impartición es presencial, conformada por sesiones de 4 horas, en las que se intercambian experiencias entre los participantes y el asesor del curso. Se realizan actividades de aprendizaje orientadas al fortalecimiento de habilidades en el manejo y diseño de la iluminación con énfasis en producciones televisivas. </p>

