        <h2><span class="color-editor">Presentación</span></h2>
        <p>La producción de programas audiovisuales tiene un proceso que, de manera general, se divide en tres etapas: preproducción, producción y posproducción. Esta última es tan importante como el inicio mismo del proyecto pues da continuidad y sentido a las imágenes y a la banda sonora, lo que permite que la narrativa audiovisual tenga una lógica. La posproducción es el momento en que se selecciona el material útil que se ha grabado para darle estructura y forma.</p>

        <p>La edición/posproducción de programas audiovisuales es un proceso dentro del cual se realiza la eliminación, cambio y adición de efectos de video y audio, con el uso de software. Es una actividad que forma parte de la producción y realización de programas de televisión y material audiovisual en el contexto educativo.</p>

        <p>El editor es el especialista que ordena y brinda proyección y sentido a los materiales de audio y video que se generan durante la producción. El editor recibe los materiales realizados y, con base en los documentos de cada proyecto (diseño de serie o programa, guion, lista de decisiones de material calificado, entre otros), lleva a cabo la ardua tarea de estructurar el discurso audiovisual para dar cumplimiento a los objetivos de cada proyecto.</p>

        <p>En la actualidad, gracias a la tecnología del video digital, la edición se puede realizar con equipos de cómputo. A este tipo de trabajo se le conoce como edición no lineal. Los editores de hoy se especializan en el manejo de software para esta clase de edición.</p>

        <p>El Centro de Capacitación Televisiva ofrece el Diplomado de Editor/Posproductor para Televisión con el fin de formar a los especialistas con conocimientos teóricos y prácticos del manejo del equipo moderno de edición, y con sentido de la organización del discurso audiovisual, con énfasis en programas educativos, que requieren las producciones contemporáneas.</p>

        <h2><a name="a"><span class="color-editor">1. Dirigido a</span></a></h2>
        <p>Docentes, comunicadores, productores y/o diseñadores de materiales educativos, capacitadores, personal técnico y de producción, estudiantes de comunicación e ingeniería, así como a personas relacionadas con la elaboración de materiales educativos preferentemente en el campo audiovisual.</p>

        <h2><a name="b"><span class="color-editor">2. Perfil de ingreso</span></a></h2>
        <p>Nivel académico mínimo de educación media superior o superior.</p>

        <h2><a name="c"><span class="color-editor">3. Perfil de egreso</span></a></h2>
        <p>Al finalizar el diplomado, el participante será capaz de editar secuencias audiovisuales, con título y transiciones en los sistemas Final CutPro, Protools y Vegas Pro.</p>

        <h2><a name="d"><span class="color-editor">4. Documento obtenido</span></a></h2>
        <h3>Diploma</h3>
        <p>Este documento respalda los conocimientos técnicos y teóricos en el campo de la edición de programas audiovisuales.</p>

        <h2><a name="e"><span class="color-editor">5. Mercado laboral</span></a></h2>
        <p>El especialista editor/posproductor para televisión puede formar parte de equipos de trabajo en televisoras, casas productoras, escuelas, instituciones gubernamentales y empresas que se dediquen a la producción audiovisual.</p>

        <h2><a name="f"><span class="color-editor">6. Duración</span></a></h2>
        <p>180 horas, equivalentes a 18 créditos del Marco Mexicano de Cualificaciones.</p>

        <h2><a name="g"><span class="color-editor">7. Competencias que se favorecen</span></a></h2>
        <h3>Competencias profesionales</h3>
        <p>Administra la posproducción de proyectos audiovisuales con base en el análisis del guion técnico, programa de trabajo y los objetivos de la producción, con el uso de software Final CutPro, Protools y Vegas Pro, en los diferentes formatos de audio y video; así como los sistemas y señales de video profesional. Toma decisiones pertinentes en la edición de programas con base en los requerimientos de producción y situaciones no previstas con la responsabilidad de desarrollar materiales de calidad para su transmisión.</p>

        <h3>Conocimientos</h3>
        <p>Aplica los principios del proceso de edición/posproducción de programas de televisión educativa, con base en el uso de software Final CutPro, Protools y Vegas Pro para audio y video y los formatos y sistemas de señales, con el cuidado de lograr la narrativa de imagen y audio, secuencia y duración acordes a los requerimientos de producción.</p>

        <h3>Habilidades</h3>
        <p>Edita material de audio y video de acuerdo con las necesidades producción de programas de televisión y material audiovisual con base en el uso de software de audio y video, atendiendo la intención de producción y la narrativa requerida para las producciones de tipo educativo.</p>

        <h3>Actitudes</h3>
        <p>Valora la importancia de la edición/posproducción de audio y video de programas de televisión en el proceso de producción. Mantiene una comunicación efectiva con el equipo de producción y se apega a los lineamientos de trabajo y colaboración. Aplica con responsabilidad las medidas de seguridad e higiene establecidas en los procesos que desarrolla.</p>

        <p>Las competencias alcanzadas corresponden al Nivel 6 del Marco Mexicano de Cualificaciones.</p>

        <h3>Competencias específicas</h3>
        <p>Las competencias a promover en cada módulo son las siguientes:</p>

        <p>Módulo I. Explica los procesos de la edición de audio y video no lineal con base en los requerimientos de producción de televisión educativa y diferentes software utilizados para la edición de audio y video, a partir del análisis de la narrativa audiovisual.</p>

        <p>Módulo II. Usa las herramientas de edición a través de software como Final CutPro, Protools y Vegas Pro para la edición de audio y video de programas de televisión con base en los formatos y sistemas de señales en los que fueron realizados.</p>

        <p>Módulo III. Edita programas de televisión y materiales audiovisuales con base en la intención educativa y los parámetros de producción para su transmisión o uso en el ámbito educativo.</p>

        <h2><a name="h"><span class="color-editor">8. Organización</span></a></h2>
        <p>El diplomado está organizado en tres módulos secuenciados, compuestos por cursos de 20 horas cada uno, cuatro cursos en el primero, tres en el segundo y dos en el tercero. El primer módulo se caracteriza por introducir al participante al proceso de edición/producción de programas de televisión educativa con base en los principios de edición de audio y video.</p>

        <p>En el módulo II se reconocen diferentes tipos de software para la edición de audio y video y sus diferentes herramientas aplicadas a la producción televisiva.</p>

        <p>En el módulo III se realiza la edición de audio y video para programas de televisión con fines educativos en HD y surround 5.1.</p>

        <h2><a name="i"><span class="color-editor">9. Metodología</span></a></h2>
        <p>Para favorecer los objetivos y competencias planteadas, el diplomado se compone de ocho cursos formativos en los que se favorece la capacidad de análisis la importancia de la edición en la narrativa audiovisual de programas educativos.</p>

        <p>La modalidad de impartición es presencial, conformada por sesiones de 4 horas cada una en las que se intercambian experiencias entre los participantes y el asesor del curso. Se realizan actividades de utilización de software orientadas al manejo de la edición.</p>

