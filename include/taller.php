<div class="row">
    <h3 class="col-md-12"><span class="azul-marino">DESCRIPCIÓN DE LA ACTIVIDAD</span></h3>
    <p class="col-md-9 ">
        Al inicio del taller los alumnos conocen los procesos de producción a través de un recorrido por las 
        instalaciones de Televisión Educativa, interactuando con las personas y equipos tecnológicos que intervienen 
        en la producción audiovisual. Posteriormente, se abre un espacio para visionar y analizar críticamente los 
        contenidos de la televisión, buscando sensibilizar a los alumnos sobre la lectura y análisis de las imágenes
        audiovisuales y sus características. Al final se implementa una estrategia pedagógica-comunicativa en la que 
        el alumno, con el apoyo de los instructores, instrumenta la producción y realización de un programa de televisión.
    </p>
