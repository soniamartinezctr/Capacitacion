    <div class="row">
        <h3 class="col-md-12"><span class="azul-marino">EJERCICIO DE PRODUCCIÓN SE ORIENTA HACÍA DOS LÍNEAS DE INTERVENCIÓN:</span></h3><br>
        <p class="col-md-9 ">
            Para el alumno de Educación Media Superior, en el marco de su proceso de Orientación Vocacional, 
            que se encuentre interesado en carreras relacionadas con este medio, identifique intereses laborales,
            el nivel de conocimiento que se requiere para desempeñar ciertas funciones, la motivación real 
            de trabajo y la relación con el mercado laboral. A través de actividades demostrativas, lúdicas y de
            reflexión, los participantes obtendrán información directa de los hábitos de trabajo y habilidades 
            sociales que se requieren para ejercer profesionalmente en el campo de la producción televisiva, y 
            así tener herramientas que faciliten su elección profesional.
            </br>
            Para que el alumno de Educación Superior, principalmente de las carreas de comunicación y educación, 
            tenga un espacio en el que a través de actividades tanto prácticas como teóricas, ponga en juego y 
            fortalezca los conocimientos y habilidades que ha desarrollado en su formación académica hasta 
            ese momento. Al final del taller, el alumno realizará una evaluación de los logros alcanzados 
            vinculándolos con el ejercicio profesional y laboral de su carrera o especialidad.  
        </p>
