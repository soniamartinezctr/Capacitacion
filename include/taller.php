        <li>Duración total: 4 horas.</li>
        <li>Número de participantes: mínimo 10, máximo 18.</li>
        <li>La actividad es gratuita y se imparte en las instalaciones del Centro de Capacitación Televisiva.</lil>
        <li>Se dirige principalmente a alumnos de Educación Media Superior y Superior interesados en el campo de la producción televisiva.</li>
        <li>Las actividades del taller que se desarrollan varían en función del nivel educativo.</li>
        <li>Aprovecha los recursos tecnológicos para fomentar las competencias comunicativas de los alumnos.</li>
        <li>Propicia la generación de ambientes de aprendizaje colaborativo para la creación de equipos de trabajo.</li>
        <li>Los responsables de la actividad están preparados para diseñar actividades didácticas y conducir grupos, además de contar con 
