                <div class="row">
                    <div class="col-md-9">
                        <h3 class="col-md-12" align ="left">Consta de seis subáreas</h3>

                        <p class="col-md-12">
                            <b>Equipamiento, mantenimiento y transmisión.</b> Está integrada por todos aquellos contenidos
                            técnicos y tecnológicos vinculados con el equipamiento, mantenimiento y funcionamiento de los
                            componentes necesarios para la producción, transmisión y recepción audiovisual.    
                        </p>
                        <p class="col-md-12">
                            <b>Realización y producción.</b> Incluye los contenidos referentes al proceso integral para desarrollar un
                            producto audiovisual.
                        </p>
                        <p class="col-md-12">
                            <b>Diseño de proyectos.</b> Incluye todos los contenidos relacionados con la estructuración y el desarrollo
                            de una idea o tratamiento del contenido educativo.    
                        </p>
                        <p class="col-md-12">
                            <b>Dramatización y conducción.</b> Estudia los elementos técnicos, creativos y artísticos de la puesta
                            en escena y la conducción de programas audiovisuales.
                        </p>
                        <p class="col-md-12">
                            <b>Administración de la producción audiovisual.</b> Estudia los procesos y mecanismos
                            administrativos y legales que deben considerarse en la producción y difusión del material audiovisual.
                        </p>
                        <p class="col-md-12">
                            <b>Ingeniería de la producción.</b> Desarrolla los contenidos tanto técnicos como artísticos vinculados
                            con la ejecución de la producción.
                        </p>


