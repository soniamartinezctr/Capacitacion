                        <h3 class="col-md-12" align ="left">Áreas que a su vez constan de diferentes subáreas</h3>
                        <h3 class="col-md-12" align ="left">Producción Audiovisual</h3>
                        <p class="col-md-9">
                            Incluye todos y cada uno de los aspectos de índole pedagógica, comunicativa, técnica, artística
                            y administrativa involucrados en la realización de un audiovisual, desde el desarrollo de la idea
                            hasta la presentación del producto.  
                        </p>  
                        <img class="hidden-xs" src="images\cete\nosotros\areas-curriculares-nosotros.png" />   
