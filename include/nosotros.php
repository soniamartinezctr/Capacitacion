                <div class="row">
                    <h3 class="col-md-12" align ="left">Áreas de conocimiento</h3>
                    <p class="col-md-9">
                        El Centro desarrolla servicios educativos en el ámbito del audiovisual y las tecnologías de la
                        información y la comunicación como la televisión, el video, la radio, el multimedia y el Internet a
                        partir de una estructura curricular amplia conformada por tres áreas de conocimiento que
                        permiten el crecimiento de acuerdo a las pautas que marcan el contexto social, educativo,
                        cultural y tecnológico. Con una filosofía constructivista, la educación del Centro se rige por el
                        principio del desarrollo de capacidades, habilidades y destrezas: enseñar-haciendo.
                    </p>
                </div>
                <br>
