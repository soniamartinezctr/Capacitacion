            <h2><span class="color-productor">Presentación</span></h2>
            <p>Todo programa televisivo, por muy sencillo que parezca, así como los anuncios publicitarios, los videos para difusión, entre otros más, tienen un proceso de producción que hace que el audiovisual cumpla con la función para la que se crea. En la pantalla se ve el producto final pero generalmente se desconoce que para llegar a ese producto, se tuvo que haber pasado por toda una serie de procesos, de revisiones, de acuerdos, de tiempos, que hicieron que el audiovisual tuviera esas características. La persona responsable de hacer que un programa llegue a ese resultado, es el productor.</p>

            <p>El productor de televisión es la figura clave en la realización de programas audiovisuales. En el caso de la televisión educativa, esta importante figura es quien coordina el enlace con especialistas y generadores de contenidos, es responsable de su correcta interpretación y traducción a una narrativa y a un lenguaje audiovisual. Cumple su función como arquitecto y diseñador de series y programas junto con sus otras funciones paralelas: la coordinación de las necesidades de la ingeniería de la producción audiovisual,[1] así como la realización de la obra para su exhibición y transmisión en pantalla.</p>

            <p>En el Centro de Capacitación Televisiva, surge el Diplomado Productor de Televisión, con él se busca formar especialistas en las funciones de esta figura central dentro de las distintas dinámicas de elaboración de videos y programas de televisión de corte educativo y cultural. Esta precisión de funciones conlleva la delimitación de competencias de orden técnico, creativo, así como de administración y gestión de proyectos.</p>

            <h2><a name="a"><span  class="color-productor">1. Dirigido a</span></a></h2>
            <p>Coordinadores de equipos de producción televisiva con habilidades de liderazgo, especialistas de contenidos educativos y culturales que desarrollan materiales en los campos de: artes y humanidades, ciencias sociales, educación, administración y derecho; ciencias naturales, exactas y de la computación; ingeniería, manufactura y construcción; agronomía y veterinaria; salud y de servicio, interesados en la producción de televisión educativa y cultural.</p>

            <h2><a name="b"><span  class="color-productor">2. Perfil de ingreso</span></a></h2>
            <p>Nivel académico de educación superior.</p>

            <h2><a name="c"><span  class="color-productor">3. Perfil de egreso</span></a></h2>
            <p>Al término del diplomado, el participante será capaz de:</p>

            <p>Diseñar propuestas de series, programas de televisión educativa y cultural de acuerdo con sus diferentes géneros, formatos y dinámicas así como administrar eficientemente los recursos financieros, técnicos y humanos del proyecto.</p>


            <h2><a name="d"><span  class="color-productor">4. Documento obtenido</span></a></h2>
            <h3>Diploma</h3>
            <p>Este documento respalda los conocimientos técnicos y teóricos en el campo de la producción de programas de televisión educativa y cultural.</p>

            <h2><a name="e"><span  class="color-productor">5. Mercado laboral</span></a></h2>
            <p>El campo de aplicación del productor va desde televisoras, casas productoras y áreas de comunicación social, hasta instituciones de enseñanza superior, tanto públicas como privadas. La generación de producción de series y programas de televisión, hoy incide en un horizonte muy amplio y diverso como el transmedia, que a su vez involucra la exhibición por internet, por dispositivos móviles y por multimedia, lo que abre un campo de acción cada vez más extenso.</p>

            <h2><a name="f"><span  class="color-productor">6. Duración</span></a></h2>
            <p>220 horas, equivalentes a 22 créditos del Marco Mexicano de Cualificaciones.</p>

            <h2><a name="g"><span  class="color-productor">7. Competencias que se favorecen</span></a></h2>
            <h3>Competencias profesionales</h3>
            <p>Gestiona y coordina los procesos para la producción de programas de televisión educativos y culturales en sus diferentes etapas: preproducción, producción y posproducción. Establece la coordinación entre el “cliente”, el equipo de producción y creativos para la elaboración de la propuesta audiovisual, con base en los objetivos planteados. Coordina el equipo de producción, staff técnico y talento. Toma decisiones y asume responsabilidades en las diferentes etapas de la producción sobre costos, tiempos, locaciones y participantes. Revisa y entrega el producto final con las características acordadas.</p>

            <h3>Conocimientos</h3>
            <p>Examina las funciones de la televisión y sus medios de producción y transmisión. Plantea propuestas de uso de múltiples medios y soportes de comunicación, de acuerdo con los objetivos propuestos. Explica los principios del proceso de producción y edición de programas de televisión educativa  y cultural en las diferentes etapas. Planea y establece las funciones del equipo de producción, creativo, staff y talento de acuerdo con las necesidades del programa audiovisual.</p>

            <h3>Habilidades</h3>
            <p>Desarrolla proyectos para programas audiovisuales educativos y culturales de los que desprende el diseño de producción que exprese el proceso creativo, técnico, realización, producción, administración y manejo de equipo humano. Coordina los procesos en las etapas de preproducción, producción y/o posproducción de programas de televisión educativa y cultural de diferentes campos del conocimiento.</p>

            <h3>Actitudes</h3>
            <p>Promueve el trabajo colaborativo en las diferentes etapas del proceso de producción. Establece un ambiente de trabajo de respeto y responsabilidad entre el equipo de trabajo. Resguarda la integridad del equipo y el personal que interviene en la producción. Prevé y gestiona en tiempo y forma los requerimientos para la producción.</p>

            <p>Las competencias alcanzadas corresponden al Nivel 7 del Marco Mexicano de Cualificaciones</p>

            <h3>Competencias específicas</h3>
            <p>Las competencias a promover en cada módulo son las siguientes:</p>

            <p>Módulo I. Analiza los fundamentos de la televisión educativa y cultural con base en la normatividad, formatos, categorías y orientación y los distingue de otros procesos de producción. Explica las etapas del proceso de producción, y sus diferencias en estudio y en locación. Propone formas de generar contenidos para los distintos recursos transmedia.</p>

            <p>Módulo II. Reconoce los elementos del lenguaje y narrativa televisiva desde el punto de vista del Director. Examina la división del trabajo, las funciones y las especialidades que intervienen en la producción, dirección y realización de un programa audiovisual. Establece las dinámicas de trabajo en las diferentes etapas de la producción.</p>

            <p>Módulo III. Discute los principios básicos de una premisa, una sinopsis, un argumento, un guion literario y uno técnico. Genera proyectos para producción con base en los elementos que distinguen una sintaxis audiovisual de una literaria. Diseña la producción de un programa piloto de televisión educativa o cultural.</p>

            <h2><a name="h"><span  class="color-productor">8. Organización</span></a></h2>
            <p>El diplomado está organizado en tres módulos secuenciados que se integran por un total de 11 cursos de 20 horas cada uno.</p>

            <p>El módulo I se compone de cuatro cursos; se caracteriza por introducir al participante al análisis histórico de la televisión educativa y cultural con base en sus características y formatos, responde a la formación de su papel como eje rector en todos los procesos de diseño y organizacionales en una institución o centro de producción educativa o cultural, que tenga como objetivo la realización de formatos de series y programas.</p>

            <p>El módulo II se compone de tres cursos en los que se reconocen los esquemas organizacionales y funcionales en los equipos de producción. Se establecen las dinámicas de trabajo de la producción y posproducción, tanto en estudio como en locación.</p>

            <p>El módulo III se integra por cuatro cursos en los que se aborda lo referente al diseño de la producción, para lo cual se revisan desde los elementos de arranque de una idea para convertirla en guion, los procesos técnicos y de realización consecutivos, hasta su aplicación en un programa piloto de televisión educativa o cultural.</p>

            <h2><a name="i"><span  class="color-productor">9. Metodología</span></a></h2>
            <p>Para favorecer los objetivos y competencias planteadas, el diplomado consta de diez cursos formativos. La modalidad de impartición es presencial, conformada por sesiones de 4 horas cada una en las que se intercambian experiencias entre los participantes y el asesor del curso. Se realizan actividades de aprendizaje orientadas al fortalecimiento de las experiencias y se culmina con la producción de un programa piloto el que se ponen en práctica todas las funciones del productor.</p>

