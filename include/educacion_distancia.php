        <div class="row gris">
                <h5 class="col-md-9 azul">TELECURSOS</h5>
                <p class="col-md-9">
                    Son programas audiovisuales con propuestas didácticas para la comprensión teórico-práctica de alguna de las áreas de conocimiento que se trabajan en el Centro. Los telecursos proporcionan una visión panorámica de un tema y cuentan con el apoyo de materiales didácticos, como guías y lecturas complementarias, que se ofrecen en línea mediante una plataforma electrónica de aprendizaje que permite atender de manera simultánea a estudiantes dispersos geográfica y temporalmente.
                </p><br>
                <p class="col-md-9">
                    Las lecciones audiovisuales que componen cada telecurso se preparan con los temas seleccionados a manera de documentales demostrativos, en una progresión lógica que vincula y explica los conceptos mediante demostraciones, gráficos y prácticas, que permiten apreciar los contenidos. Gracias a las guías didácticas el participante logra la comprensión de las secuencias para los ejercicios y adquiere información específica sobre los conceptos para realizar los ejercicios propuestos
                </p><br>
                <p class="col-md-9">
                   El asesor realiza un acompañamiento para enlazar los contenidos audiovisuales con los materiales didácticos, resuelve dudas, modera los foros para los intercambios de opinión y prepara al grupo para que se realice la evaluación.

                   Nuestros asesores son especialistas en cada una de las áreas de conocimiento y dominan la metodología del trabajo a distancia para brindar adecuadas asesorías, retroalimentación y evaluación del aprendizaje de los estudiantes.
                </p><br>
                <h5 class="col-md-9 azul">TELESEMINARIOS</h5>
                <p class="col-md-9">
                    Es un formato audiovisual educativo cuyo propósito es generar o presentar ideas novedosas, profundizar en el conocimiento de temas específicos o muy especializados o renovar enfoques en determinadas áreas de conocimiento entre otras funciones. Se compone de la participación de varios especialistas que en el mismo espacio/tiempo televisivo desarrollan los contenidos del programa.
                </p><br>
                <p class="col-md-9">
                    Mediante mecanismos a distancia para la discusión, retroalimentación y evaluación, los teleseminarios contribuyen a la actualización y profundización de los conocimientos relacionados con un determinado tema del ámbito educativo.
                </p><br>
                <h5 class="col-md-9 azul">DIPLOMADOS</h5>
                <p class="col-md-9">
                    Diseñados para la actualización y especialización en las áreas de conocimiento que abordamos; estos tienen una estructura de base modular suficientemente flexible para adaptarse a los contenidos que se van a desarrollar.
                </p><br>
                <p class="col-md-9">
                    Los diplomados se basan en el uso del trabajo en línea con un apoyo intensivo de asesoría académica, y tienen como principal estrategia instruccional el desarrollo de proyectos.
                </p><br>                
                <h5 class="col-md-9 azul">TELECONFERENCIAS</h5>
                <p class="col-md-9">
                    Es la exposición detallada de un tema o asunto tratado por un especialista que los expone a una audiencia televisiva. La función principal de este recurso es la actualización en los campos de conocimiento del Centro, particularmente en el área de educación y medios audiovisuales.
                </p><br>
                <p class="col-md-9">
                    Es frecuente certificar esta clase de servicios mediante diversos mecanismos de interacción electrónica entre estudiantes y especialistas.
                </p>
