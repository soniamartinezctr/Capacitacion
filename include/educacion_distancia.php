            <h3 class="col-md-12 azul"><span class="azul">SERVICIOS EDUCATIVOS</span></h3>
            <p class="col-md-9"> 
                En el Centro ofrecemos servicios de educación continua que consideran un diseño particular de estrategia instruccional, asesoría y evaluación, centrada en la televisión y la computadora posibilitando la atención simultánea de participantes dispersos geográfica y temporalmente.
            </p><br>
            <p class="col-md-9">
                Nuestros servicios son certificables y se ofrecen a partir de estrategias didácticas como:
            </p>
