        <h2 class="color_doc"><span class="color-audiovisual">Presentación</span></h2>
        <p>En todo el mundo se generan a diario miles y miles de imágenes en movimiento y sonidos, que son testigos de cada época y vivencias del acontecer humano. Sin embargo, estos registros no siempre son conservados ni valorados en toda su dimensión y potencialidad. De aquí la necesidad de contar con acervos sobre ellos, de tal manera que puedan ser localizables para ser utilizados en diferentes momentos. La custodia, conservación, preservación y rescate del material audiovisual forma parte de la memoria de la humanidad, es el patrimonio de cada nación.</p>

        <p>El documentalista audiovisual es el especialista encargado del manejo, cuidado, clasificación, resguardo y difusión de acervos audiovisuales. Su labor, en cualquier espacio que cuente con estos productos, permite el acercamiento de los usuarios a materiales diversos con información puntual acerca de su contenido y formatos de presentación que faciliten la identificación clara de sus posibilidades de uso como herramientas para el logro de objetivos en diferentes ámbitos, escolares, de capacitación, formativos y de entretenimiento, investigación, entre otros.</p>

        <p>El Centro de Capacitación Televisiva, de la Dirección General de Televisión Educativa, en respuesta a las necesidades provocadas por los acelerados y profundos cambios en la manera de almacenar, acceder y difundir la información de documentos audiovisuales, ha diseñado el Diplomado Documentalista Audiovisual en línea; programa innovador en México que pone a disposición de los interesados la posibilidad de formarse en este ámbito, sin necesidad de desplazarse a nuestras instalaciones, permitiendo a los participantes, tener acceso a una formación de calidad, con independencia del tiempo y las distancias. </p>

        <h2 class="color_doc"><a name="a"><span class="color-audiovisual">1. Dirigido a</span></a></h2>
        <p>Profesionales de la comunicación, la documentación audiovisual (DAv) y la bibliotecología, maestros, documentalistas, informáticos, archivólogos, diseñadores web y arquitectos de la información, involucrados en la elaboración, conservación y difusión de los documentos audiovisuales y personas interesadas en el cuidado, organización y posibilidades de uso de documentos audiovisuales en escuelas e instituciones.</p>

        <h2 class="color_doc"><a name="b"><span class="color-audiovisual">2. Perfil de ingreso</span></a></h2>
        <p>Nivel académico de educación superior.</p>

        <h2 class="color_doc"><a name="c"><span class="color-audiovisual">3. Perfil de egreso</span></a></h2>
        <p>Al finalizar el diplomado, el participante será capaz de:</p>

        <p>Aplicar los fundamentos técnico-procedimentales para la conservación, preservación, organización y digitalización de materiales audiovisuales.</p>

        <h2 class="color_doc"><a name="d"><span class="color-audiovisual">4. Documento obtenido</span></a></h2>
        <h3>Diploma</h3>
        <p>Este documento respalda los conocimientos y conceptos técnicos y teóricos, en el área laboral de documentación audiovisual.</p>

        <h2 class="color_doc"><a name="e"><span class="color-audiovisual">5. Mercado laboral</span></a></h2>
        <p>El egresado del Diplomado en Documentación Audiovisual puede desempeñarse profesionalmente en repositorios de documentos audiovisuales, bibliotecas, fonotecas, fototecas, mapotecas, cinetecas, videotecas y centros de documentación audiovisual en general.</p>

        <h2 class="color_doc"><a name="f"><span class="color-audiovisual">6. Duración</span></h2>
        <p>240 horas, equivalentes a 24 créditos del Marco Mexicano de Cualificaciones</p>

        <h2 class="color_doc"><a name="g"><span class="color-audiovisual">7. Competencias que se favorecen</span></a></h2>
        <h3>Competencias profesionales</h3>
        <p>Gestiona los procesos para la clasificación y conservación de la información audiovisual, con base en la normatividad vigente. Propone formas de acceso y organización de materiales audiovisuales en sus diferentes formatos. Toma decisiones sobre el proceso de custodia, conservación, preservación y rescate de los recursos audiovisuales. Organiza y da seguimiento a los equipos de trabajo que participan en el proceso. Planea y ejecuta proyectos de servicios de información y documentación con un sentido ético y considerando los derechos de autor.</p>

        <h3>Conocimientos</h3>
        <p>Explica la normatividad vigente en la clasificación y conservación de acervos audiovisuales. Elabora propuestas que faciliten el registro y acceso a la información, tomando en cuenta algunas de las necesidades de acceso a los materiales desde distintos campos de utilidad.</p>

        <h3>Habilidades</h3>
        <p>Diseña procesos para la clasificación de audiovisuales, con base en la normatividad vigente. Organiza las fuentes de información audiovisual conforme a los criterios establecidos. Utiliza técnicas y TIC para la organización, selección, evaluación, conservación y migración de archivos audiovisuales.</p>

        <h3>Actitudes</h3>
        <p>Valora la importancia de la DAv y el impacto de los archivos digitales en las actividades humanas, así como los resultados que se obtienen a través del uso de nuevas tecnologías, en la restauración, preservación y utilización de los documentos digitales como herramientas didácticas. Difunde la DAv en la generación de nuevos materiales. </p>

        <p>Las competencias alcanzadas corresponden al Nivel 7 del Marco Mexicano de Cualificaciones.</p>

        <h3>Competencias específicas</h3>
        <p>Las competencias a promover en cada módulo son las siguientes:</p>

        <p>Módulo I. Reconoce la naturaleza de la documentación, la información y los documentos sonoros y audiovisuales.</p>

        <p>Módulo II. Diseña estrategias para la evaluación, preservación, conservación y migración de archivos analógicos a digitales, del patrimonio documental contenido en las colecciones sonoras y audiovisuales.</p>

        <p>Módulo III. Administra la información para la recopilación, organización y difusión y elabora los procedimientos para la utilización, distribución y trasmisión, así como para la formación de colecciones.</p>

        <h2 class="color_doc"><a name="h"><span class="color-audiovisual">8. Organización</span></a></h2>
        <p>El diplomado está organizado en tres módulos secuenciados e integrados, a su vez, por diferentes cursos cada uno. Se requiere un tiempo de trabajo de 80 horas por módulo que concluyen con la realización de una propuesta aplicable a la DAv.</p>

        <p>Se estructura por los siguientes módulos:</p>

        <p>Módulo I: en el que se analizan los fundamentos y características de la documentación audiovisual, así como los lenguajes sonoros y audiovisuales; se finaliza con la revisión de la legislación y políticas de uso que permitan darle el tratamiento adecuado a cada materia.</p>

        <p>Módulo II: en donde se estudia la importancia y naturaleza de los centros de información, la organización física de los mismos, las técnicas utilizadas para la organización y agrupación, así como el impacto de las nuevas tecnologías en la documentación audiovisual.</p>

        <p>Módulo III: se señalan los tipos de soportes y formatos, las causas frecuentes de deterioro en los materiales, las normas y procedimientos para su cuidado, así como lo referente con el almacenaje. Se advierten también los procedimientos y herramientas para la digitalización de los documentos audiovisuales.</p>

        <h2 class="color_doc"><a name="i"><span class="color-audiovisual">9. Metodología</span></a></h2>
        <p>El diplomado en línea se ofrece a través de la plataforma e-learning Moodle. Los participantes cuentan con materiales didácticos, tales como: Guías de aprendizaje, en las que se contemplan los objetivos esperados, actividades de aprendizaje, reflexiones, lecturas recomendadas y actividades de autoevaluación. Además de contar con asesoría de expertos en cada uno de los temas, antología de textos y la riqueza que ofrece la participación en los foros de discusión.</p>


