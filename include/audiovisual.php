        <h2 class="color_doc"><a name="k"><span class="color-audiovisual">11. Programa de estudio</span></a></h2>
        <h2 class="color_doc"><span class="color-audiovisual">Módulo I. La Documentación Audiovisual</span></h2>

        <p>1. Fundamentos, historia y metadocumentación, 20 horas</p>
        <p>2. Lenguajes sonoros y audiovisuales, 20 horas</p>
        <p>3. Cultura sonora y audiovisual,* 20 horas</p>
        <p>4. Legislación y políticas de uso (adquisición, transmisión y acceso), 20 horas</p>


        <h2 class="color_doc"><span class="color-audiovisual">Módulo II. Procesos documentales</span></h2>

        <p>1. El Centro de Documentación, 20 horas</p>
        <p>2. Catalogación, 20 horas</p>
        <p>3. Clasificación e indización, 20 horas</p>
        <p>4. Tesauros, 20 horas</p>            


        <h2 class="color_doc"><span class="color-audiovisual">Módulo III. Conservación y preservación</span></h2>

        <p>1. Soportes, formatos y digitalización de materiales audiovisuales, 20 horas </p>
        <p>2. Conservación y almacenamiento, 20 horas</p>
        <p>3. Políticas de manejo del recurso (uso, respaldo y copia) y técnicas de restauración, 20 horas</p>
        <p>4. Uso social de los repositorios digitales audiovisuales: explotación, educación, investigación, comunicación y divulgación, 20 horas</p>

        <p>* Educación para la preservación del patrimonio sonoro y audiovisual</p>

