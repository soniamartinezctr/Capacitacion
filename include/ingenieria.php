                <h2><a name="a"><span class="color-ingenieria">1. Dirigido a</span></a></h2>
                <p>Productores, realizadores, técnicos e interesados en el trabajo de ingeniería de la producción (cámara, iluminación, audio, control de video y edición).</p>

                <h2><a name="b"><span class="color-ingenieria">2. Perfil de ingreso</span></a></h2>
                <p>Nivel académico mínimo de educación media superior o superior.</p>
                <h2><a name="c"><span class="color-ingenieria">3. Perfil de egreso</a></h2>
                <p>Al término del diplomado, el participante será capaz de:</p>

                <p>Operar los equipos adecuados para la realización de programas de televisión en estudio.
                <p>Coordinar y enlazar entre sí el trabajo de todas las áreas de la ingeniería de la producción en combinación con el personal de la producción, para la realización de programas de televisión.</p>

                <h2><a name="d"><span class="color-ingenieria">4. Documento obtenido</span></a></h2>
                <h3>Diploma</h3>
                <p>Este documento respalda los conocimientos técnicos y teóricos, necesarios para el desempeño idóneo del camarógrafo de televisión en estudio y locación.</p>

                <h2><a name="e"><span class="color-ingenieria">5. Mercado laboral</span></a></h2>
                <p>El profesional de este campo puede laborar, como responsable de cualquiera de las áreas de la ingeniería de la producción (operación de cámara, audio, iluminación, switcher y control de video) y/o como coordinador técnico, en empresas o instituciones productoras de televisión o video, públicas o privadas.</p>

                <h2><a name="f"><span class="color-ingenieria">6. Duración</span></a></h2>
                <p>200 horas, equivalentes a 20 créditos del Marco Mexicano de Cualificaciones.</p>
