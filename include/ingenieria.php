                <h2><a name="g"><span class="color-ingenieria">7. Competencias que se favorecen</span></a></h2>
                <h3>Competencias profesionales</h3>
                <p>Administra el trabajo de manera coordinada con el equipo de camarógrafos, iluminadores, operadores de audio, switchers y controladores de video, en proyectos audiovisuales. Toma decisiones que permitan la consecución de los objetivos de trabajo con el responsable de la producción, asumiendo una actitud comprometida y de participación. Posibilita el desenvolvimiento de los integrantes del proyecto, para facilitar las actividades, tanto individuales como de grupo.</p>

                <h3>Conocimientos</h3>
                <p>Explica la operación, funcionamiento y características de los equipos para la realización de programas de televisión en estudio. Conocimientos de las técnicas y los métodos de trabajo de la ingeniería de producción audiovisual en estudio. Desarrolla la comprensión crítica de los elementos de producción y edición del lenguaje audiovisual.</p>

                <h3>Habilidades</h3>
                <p>Planea y ejecuta el trabajo de la ingeniería de producción considerando todas las áreas que la comprenden. Maneja el lenguaje audiovisual y su relación con la ingeniería de la producción. Resuelve situaciones imprevistas relacionadas con el funcionamiento del equipo y el tipo de transmisión.</p>

                <h3>Actitudes</h3>
                <p>Reconoce el papel que juega la ingeniería de producción dentro del proceso de producción de programas de televisión en estudio. Comunica de forma eficiente las necesidades sobre la labor que desempeña en el flujo de trabajo integral. Mantiene un ambiente de trabajo cordial a partir del respeto y trabajo colaborativo con el equipo de producción. Mantiene las condiciones de seguridad e higiene del equipo.

                <p>Las competencias alcanzadas corresponden al Nivel 6 del Marco Mexicano de Cualificaciones.</p>

                <h3>Competencias específicas</h3>
                <p>Las competencias a promover en cada módulo son las siguientes:</p>

                <p>Módulo I. Define qué es la ingeniería de la producción y sus etapas, así como las generalidades de cada una de las áreas que la integran. Aplica las técnicas de operación de cámara, iluminación, audio, switcher y control de video. Explica los elementos necesarios, como el espacio (planos, ángulos, iluminación), el movimiento (interno, externo y por edición), y la relación imagen-sonido (voz, música y efectos) para construir un discurso audiovisual.</p>
                <p>Módulo II. Aplique los elementos técnico-artísticos generales relacionados con la operación de cámara y la iluminación el audio, el switcheo, y el control de video.</p>
                <p>Módulo III. Se desenvuelve en el proceso de trabajo de cada una de las áreas que conforman la ingeniería de la producción en la realización de un programa de televisión informativo.</p>
