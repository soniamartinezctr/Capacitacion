                <h2><span class="color-ingenieria">Presentación</span></h2>
                <p>La ingeniería de producción audiovisual se refiere a la integración de las áreas especializadas en audio, iluminación, cámara, switcher y control de video, quienes se incorporan en los procesos orientados al manejo adecuado de las técnicas y los equipos que permiten la realización de programas de televisión en coordinación con el equipo de producción. Llevar a cabo estos procesos requiere de la reunión de manera conjunta de un grupo de especialistas que cuenten con conocimientos y habilidades para desempeñarse en las diferentes áreas bajo un objetivo común: participar en la producción de un mensaje audiovisual con óptima calidad técnica y de contenido.</p>
                <p>En el trabajo cotidiano de la ingeniería de producción que se lleva a cabo en México, es común encontrarnos con personal que, de manera indistinta y por características propias de la institución, se hace responsable de realizar diferentes funciones como grabar las imágenes, iluminar la escena, registrar el sonido, medir los parámetros de calidad de la señal o editar las imágenes recabadas, por lo que es necesario que, para el logro de un producto óptimo, todos los involucrados conozcan el trabajo realizado por los demás; por ejemplo, para grabar una imagen se requiere no sólo que el camarógrafo sepa registrarla con los parámetros técnicos y estéticos correspondientes, también es necesaria una iluminación que recree la atmósfera adecuada, un audio que genere el ambiente necesario, una señal que contemple los estándares de calidad y una selección por medio del switcher que una planos y secuencias de manera coherente con base en el objetivo del programa.</p>
                <p>Conscientes de la necesidad por el trabajo interrelacionado, el Centro de Capacitación Televisiva ofrece el Diplomado en Ingeniería de Producción Audiovisual, para preparar a quienes participen en él con los conocimientos generales del proceso de realización desde una perspectiva de trabajo integral en: operación de la cámara, iluminación, operación de audio, switcher y control de video.</p>                        
