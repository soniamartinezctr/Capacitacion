                <h2><a name="h"><span class="color-ingenieria">8. Organización</span></a></h2>
                <p>El diplomado está organizado en tres módulos secuenciados, el primero, compuesto por un curso de 40 horas y dos de 20 horas cada uno, responde a la formación básica que debe conocer un equipo de producción, donde se imparten los fundamentos de las especialidades que integran la ingeniería de producción, así como de lenguaje audiovisual.</p>

                <p>El módulo II se compone de dos cursos de 40 horas de duración cada uno en donde se explican y se ponen en práctica los elementos técnico-artísticos de cada una de las áreas involucradas en la ingeniería de producción.</p>

                <p>En el módulo III, se compone por un taller de 40 horas en el que se busca que el participante realice prácticas para la comprensión del flujo de trabajo en el marco de la ingeniería de la producción, se desempeñe con correspondencia respecto a los requerimientos del contexto y los objetivos de grabación, con base en la implementación de procedimientos de seguridad e higiene, así como los establecidos en el guion técnico.</p>
                <h2><a name="i"><span class="color-ingenieria">9. Metodología</span></a></h2>
                <p>Para favorecer los objetivos y competencias planteadas, el diplomado parte de cursos y talleres teórico-prácticos en los que el participante se desempeñará con base en los conocimientos adquiridos y en el seguimiento de procedimientos y lineamientos de calidad.</p>

                <p>La modalidad de impartición es presencial, conformada por sesiones de 4 horas, en las que se intercambian experiencias entre los participantes y el asesor del curso. Se realizan actividades de aprendizaje orientadas al fortalecimiento de habilidades en el manejo y diseño de la producción audiovisual.</p>

